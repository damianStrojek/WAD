            <h2>Electronical Sports</h2>
            <img src="static/img/katowice2019_1.png" class="centerPhoto" alt="Katowice 2019" title="Katowice 2019">
            <p>
                <b>ESPORT</b> (electronical sports) is one of the fastest growing industries in the 21st century.
                Every year, millions of people learn what esport is and want to get closer to it. Now even people from the older generation know what is electronical sport.
                There are many similarities to traditional sports, such as football, in esports. These are, among others: competition, masterful skills,
                thousands of hours of training and extreme emotions of both players and viewers.
            </p>
        </section>
        <section class="text" id="BackToBeginning">
            <h3>It began with Arcades</h3>
            <p>
                The first tournaments were played right after the creation of the first arcade games.
                Already in 1978 arcade tournament were held in which players competed. In those days there were only tournaments within,
                for example, one university. As the internet grew, so did the possibilities. The real breakthrough for esports was the evolution of computers and games.
                Games such as Street Fighter II or the first Counter Strike made playing computer games popular not only for fun.
            </p>
            <p>
                At the very beginning it was very unpopular. 
                If as a teenager you wanted to play any computer games then the easiest way was to go to an internet cafe. 
                This is how the first semi-professional teams were created.
            </p>
        </section>
        <section class="text">
            <h3>Playing games as a career</h3>
            <img src="static/img/player.jpg" class="left" title="Shox IEM Katowice 2019" alt="Shox IEM Katowice 2019">
            <p>
                Over time, esports developed more and more and more. People began to see in it hope for a better tomorrow. 
                The best of the best were, of course, players who sacrificed everything, 
                from an early age they were passionate about MOBA games (multiplayer online battle area), 
                shooters or arcade games.
            </p>
            <p>
                These people spent hours training, learning about tactics, developing their perception and strategic thinking skills.
                One of the most important elements of games is muscle memory or reflexes, which are close to perfect on a professional level.
            </p>
            <p>
                <b>But how does it all work?</b> Players who have the potential to be the best are very often found by other players in the online games. 
                When they play at home or even stream their games. While such players gain popularity through their skills, organizations begin to take an interest in them.
                Eventually, one of hundreds of organizations may speak to outstanding representatives of a given game and propose cooperation.
            </p>
            <p>
                <b>Streaming/YouTube</b> Streaming is broadcasting how you play the game. The best streamers are able to earn thousands of dollars a day and encourage hundreds
                of thousands of viewers in front of monitors daily. Their earnings include donates (gifts from viewers), subscriptions, money from the platform they stream on and payment
                from sponsors, advertising.
            </p>
            <p>
                <b>Playing proffesionally</b> Playing professionally is definitely more complicated. In this case, one team includes dozens of people, of which only + - 5 
                people are people who play on the stage, in front of the monitors. They have planned trips to other countries for the whole year and train 5 days a week, and
                when they do not train, they play for themselves to be the best of the best. They get a normal payout and bonuses that they win by winning tournaments and being higher
                and higher in the ranking of the best teams (for example - in the world).
            </p>
            <p>
                <b>Social media</b> Cooperation may also consist only in advertising, e.g. on social media. Social media includes places such as instagram, facebook and reddit.
                In this case, the contract consists in posting a certain number of things that are sponsored within, for example, a month.
            </p>
        </section>
        <section class="text">
            <h3>Polish teams</h3>
            <p>
                I will focus on the Counter Strike scene in Poland, which I know best and which I have been following since 2014, i.e. golden age. 
                <b>The Golden Age</b> is a few years in which Poles were considered the best in the world in this particular shooter,
                and in 2014 in Katowice they won the world championship. Unfortunately, this era stopped after two years and the scene was dominated by
                the Swedish team Astralis. Poles took different names of different organizations, but they are best known after 
                the name <a href="https://en.wikipedia.org/wiki/Virtus.pro" target="_blank"><b style="color: red;">Virtus Pro</b></a>.
                A year ago, this Russian organization ended cooperation with a Polish team that did not have satisfactory achievements.
            </p>
