            <h3>League of Legends</h3> 
            <p> 
                (abbreviated LoL or League) is a 2009 multiplayer online battle arena video game developed and published by Riot Games for
                Microsoft Windows and macOS. Originally inspired by Defense of the Ancients, the game has followed a freemium model since its release on October 27,
                2009. League of Legends is often cited as the world's largest esport, with an international competitive scene. The 2019 League of Legends World Championship
                had over 100 million unique viewers, peaking at a concurrent viewership of 44 million, with a minimum prize pool of US$2.5 million.
            </p>
