            <p>
                As in other multiplayer online battle arena (MOBA) games, each player in League of Legends controls a character ("champion")
                with a set of unique abilities. Most games involve two teams of five players, with each player using a different champion.
                The two teams compete to be the first to destroy the Nexus structure within the opposing base.
                Over the course of each game, champions become stronger and gain additional abilities by earning experience and thereby levelling up.
                Experience is earned by killing enemies (or being nearby when a teammate does). Champions also build strength over the course of the game 
                by buying progressively more powerful items using gold, which is earned by killing non-player enemies, killing or assisting in killing enemy
